long long totalSubsidy(const vector<int>& A, long long x) {
    long long total = 0;
    for (int Ai : A) {
        total += min(static_cast<long long>(Ai), x);
    }
    return total;
}

int main() {
    int N;
    long long M;
    cin >> N >> M;
    vector<int> A(N);
    
    long long totalA = 0;
    for (int i = 0; i < N; ++i) {
        cin >> A[i];
        totalA += A[i];
    }
    
    if (totalA <= M) {
        cout << "infinite" << endl;
        return 0;
    }
    
    long long left = 0;
    long long right = *max_element(A.begin(), A.end());
    long long maxSubsidyLimit = 0;
    
    while (left <= right) {
        long long mid = left + (right - left) / 2;
        long long totalCost = totalSubsidy(A, mid);
        
        if (totalCost <= M) {
            maxSubsidyLimit = mid;
            left = mid + 1;
        } 
        else {
            right = mid - 1;
        }
    }
    
    cout << maxSubsidyLimit << endl;
}
