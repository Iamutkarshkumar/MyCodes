#include <iostream>
#include <vector>
#include <queue>
using namespace std;

void solve() {
    long long size;
    cin >> size;
    vector<long long> numbers(size);

    for (int i = 0; i < size; i++) {
        cin >> numbers[i];
    }

    priority_queue<long long> maxHeap;
    for (auto num : numbers) {
        maxHeap.push(num);
    }

    maxHeap.pop();
    long long secondMax = maxHeap.top();

    for (int i = 0; i < size; i++) {
        if (numbers[i] == secondMax) {
            cout << i + 1 << endl;
            break;
        }
    }
}

int main() {
    solve();
    return 0;
}
