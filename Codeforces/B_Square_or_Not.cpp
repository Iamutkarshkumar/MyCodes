bool help(const vector<vector<char>>& v, int k) {
    for (int i=0;i<k;i++) {
        if (v[0][i]!='1' or v[k-1][i]!='1') return false; 
        if (v[i][0] !='1' or v[i][k-1]!='1') return false; 
    }
    for (int i = 1; i < k-1; ++i) {
        for (int j = 1; j < k-1; ++j) {
            if (v[i][j] !='0') return false;
        }
    }
    return true;
}
int main() {
    int t;
    cin >> t;
    while (t--) {
        int n;cin>>n;
        string s;cin>>s;
        int k = sqrt(n);
        if (k*k != n) {
            cout<<"No"<<endl;
            continue;
        }
        
        vector<vector<char>> v(k, vector<char>(k));
        for (int i=0;i<k;i++) {
            for (int j=0; j<k; j++) {
                v[i][j]= s[i*k + j];
            }
        }
        
        if (help(v, k)) cout<<"Yes"<<endl;
        else cout<<"No"<<endl;
    }
}



