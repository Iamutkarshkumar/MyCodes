struct Dish {
    int sweetness;
    int saltiness;
};

int main() {
    int N, X, Y;
    cin >> N >> X >> Y;

    vector<Dish> dishes(N);
    for (int i = 0; i < N; i++) {
        cin >> dishes[i].sweetness;
    }
    for (int i = 0; i < N; i++) {
        cin >> dishes[i].saltiness;
    }

    int total_sweetness = 0;
    int total_saltiness = 0;
    int num_dishes = 0;

    for (int i = 0; i < N; i++) {
        total_sweetness += dishes[i].sweetness;
        total_saltiness += dishes[i].saltiness;
        num_dishes++;
        
        if (total_sweetness > X || total_saltiness > Y) {
            break;
        }
    }

    // Print the number of dishes he will eat
    cout << num_dishes << endl;

    return 0;
}