#include <bits/stdc++.h>
using namespace std;
int main(){
    bool flag=true;
    string s;
    getline(cin,s);
    stack<int> x;
    
    for(int i=0;i<s.length();i++){
        if(s[i]==')' && x.size()==0){ 
            flag=false;
            break;
        }
        else if(s[i]=='(') x.push(1);
        else if(s[i]==')') x.pop();
    }
    if(flag==false) cout<<" not balanced "<<endl;
    else if(x.size()==0) cout<<" balanced "<<endl;
    else cout<<" not balanced "<<endl;

}


